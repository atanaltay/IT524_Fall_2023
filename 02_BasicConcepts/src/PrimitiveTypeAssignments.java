public class PrimitiveTypeAssignments {
    public static void main(String[] args) {


        //Primitive type assignments


        int x1 = 100;

        int x2 = 150;

        //When a pritimitve types is assigned
        // to another prim. type, its value is copied!
        x2 = x1;


        x1+=100;

        System.out.printf("x1:%d\n",x1);
        System.out.printf("x2:%d\n",x2);



    }
}